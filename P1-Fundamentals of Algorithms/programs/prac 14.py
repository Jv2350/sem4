def selection_sort(arr):
    n = len(arr)
    for i in range(n):
        min_index = i
        for j in range(i + 1, n):
            if arr[j] < arr[min_index]:
                min_index = j
        arr[i], arr[min_index] = arr[min_index], arr[i]
    return arr


def insertion_sort(arr):
    n = len(arr)
    for i in range(1, n):
        key = arr[i]
        j = i - 1
        while j >= 0 and key < arr[j]:
            arr[j + 1] = arr[j]
            j -= 1
        arr[j + 1] = key
    return arr


# Example usage:
arr = [64, 25, 12, 22, 11]
print("Original Array:", arr)
print("Array sorted using Selection Sort:", selection_sort(arr.copy()))
print("Array sorted using Insertion Sort:", insertion_sort(arr.copy()))
